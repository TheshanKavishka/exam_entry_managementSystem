    {
        $year = $request->year;
        $sem = $request->sem;
        $gs = $request->gs;

        $id = Auth::user()->email;

        $dep = DB::select("select faculty from students where email = ?", [$id]);

        $courses = DB::select('select * from courses where year = ? && semester = ? && genorspec = ? && faculty = ?', [$year,$sem,$gs,$dep[0]->faculty]);

        return view('student/stdashboard', compact('courses'));

    }

